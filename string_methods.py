name = "John Doe"

print(len(name)) #length
print(name.find("J")) #find character
print(name.capitalize()) #capitalize the first letter of the string
print(name.upper()) #uppercase
print(name.lower()) #lowercase
print(name.isdigit()) #return boolean if it's digit
print(name.isalpha()) #return boolean if it's alphabet
print(name.count("o")) #count character
print(name.replace("o", "a")) #replace character
print(name*4) #print the string a the number time